public class User {
    private String name;
    private int age;
    private String tell;

    public int getAge() {
        return age;
    }

    public String getName() {
        return name;
    }

    public String getTell() {
        return tell;
    }

    public void setTell(String tell) {
        this.tell = tell;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setAge(int age) {
        this.age = age;
    }
}
